// 음식 이름 → 대표 이모지 (키워드 매칭)
// 실사진은 추후 foods 데이터에 image 필드를 추가하거나 이미지 API 키를 붙여 확장.

const EMOJI_RULES: Array<[RegExp, string]> = [
    [/찌개|전골|탕|국밥|해장|순두부/, '🍲'],
    // 파스타류는 면류 규칙(🍜)보다 먼저 검사해야 🍝로 매칭된다.
    [/파스타|스파게티|리조또/, '🍝'],
    [/국수|쌀국수|우동|라멘|라면|냉면|면/, '🍜'],
    [/피자/, '🍕'],
    [/치킨|닭|통닭/, '🍗'],
    [/버거|햄버거/, '🍔'],
    [/스시|초밥|사시미|회|연어|광어/, '🍣'],
    [/샐러드/, '🥗'],
    [/스테이크|소고기|삼겹|갈비|구이|불고기|바베큐|고기/, '🥩'],
    [/카레|커리|마살라|탄두리/, '🍛'],
    [/타코|부리또|퀘사디아|나초/, '🌮'],
    [/만두|딤섬|교자/, '🥟'],
    [/떡볶이|순대|분식|떡/, '🍢'],
    [/김밥|주먹밥|덮밥|비빔밥|볶음밥|밥/, '🍚'],
    [/샌드위치|토스트|베이글|빵|브런치/, '🥪'],
    [/케이크|디저트|아이스크림|요거트|와플|크로플|마카롱/, '🍰'],
    [/커피|라떼|음료|차|에이드|스무디/, '☕'],
    [/수프|스프|포타주/, '🥣'],
    [/계란|오믈렛|에그/, '🍳'],
    [/새우|해물|해산물|조개|문어|낙지/, '🦐'],
    [/족발|보쌈/, '🍖'],
];

export function foodEmoji(name: string): string {
    if (!name) return '🍽️';
    for (const [re, emoji] of EMOJI_RULES) {
        if (re.test(name)) return emoji;
    }
    return '🍽️';
}
